package com.shanlin.p2p.app.model.enums;

/**
 * 标担保模式
 * @author zheng xin
 * @createDate 2015年2月3日
 */
public enum BidAssureMode {
	/** 
     * 本息全额担保
     */
    BXQEDB("本息全额担保"),

    /** 
     * 本金全额担保
     */
    BJQEDB("本金全额担保");

    protected final String chineseName;

    private BidAssureMode(String chineseName){
        this.chineseName = chineseName;
    }
    /**
     * 获取中文名称.
     * 
     * @return {@link String}
     */
    public String getChineseName() {
        return chineseName;
    }
}
